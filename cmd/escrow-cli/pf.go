package main

import (
	"flag"
	"fmt"
	"os/exec"
	"strings"
)

var registryHosts = map[string][]string{
	"npm":      {"registry.npmjs.org", "npm.pkg.github.com"},
	"pypi":     {"pypi.org", "files.pythonhosted.org"},
	"go":       {"proxy.golang.org", "sum.golang.org"},
	"cargo":    {"static.crates.io", "index.crates.io"},
	"nuget":    {"api.nuget.org"},
	"maven":    {"repo.maven.apache.org", "repo1.maven.org"},
	"composer": {"repo.packagist.org", "packagist.org"},
}

// allEcosystems in a stable order for deterministic output.
var allEcosystems = []string{"npm", "pypi", "go", "cargo", "nuget", "maven", "composer"}

func runPfEnable(args []string) {
	fs := flag.NewFlagSet("pf-enable", flag.ExitOnError)
	ecosystems := fs.String("ecosystems", strings.Join(allEcosystems, ","), "comma-separated ecosystems to intercept")
	proxyPort := fs.Int("proxy-port", 7888, "escrow proxy port")
	proxyUser := fs.String("proxy-user", "_escrow", "OS user running the escrow proxy")
	fs.Parse(args) //nolint:errcheck

	requireRoot("pf-enable")

	ecos := parseEcosystems(*ecosystems)
	if len(ecos) == 0 {
		die("no valid ecosystems specified; valid values: %s", strings.Join(allEcosystems, ", "))
	}

	rules := buildRules(ecos, *proxyPort, *proxyUser)
	if err := writeAtomic(pfAnchorFile, []byte(rules), 0644); err != nil {
		die("writing anchor file: %v", err)
	}
	if out, err := exec.Command("pfctl", "-a", "escrow", "-f", pfAnchorFile).CombinedOutput(); err != nil {
		die("loading pf anchor: %v\n%s", err, strings.TrimSpace(string(out)))
	}
	if out, err := exec.Command("pfctl", "-E").CombinedOutput(); err != nil {
		die("enabling pf: %v\n%s", err, strings.TrimSpace(string(out)))
	}

	fmt.Printf("pf enabled for: %s\n", strings.Join(ecos, ", "))
}

func runPfDisable(args []string) {
	fs := flag.NewFlagSet("pf-disable", flag.ExitOnError)
	fs.Parse(args) //nolint:errcheck

	requireRoot("pf-disable")

	empty := "# Escrow pf anchor — managed by escrow-cli\n"
	if err := writeAtomic(pfAnchorFile, []byte(empty), 0644); err != nil {
		die("clearing anchor file: %v", err)
	}
	if out, err := exec.Command("pfctl", "-a", "escrow", "-F", "all").CombinedOutput(); err != nil {
		die("flushing pf anchor: %v\n%s", err, strings.TrimSpace(string(out)))
	}
	// Release the pf reference acquired by -E; ignore errors (token-less release is best-effort).
	exec.Command("pfctl", "-X").Run() //nolint:errcheck

	fmt.Println("pf disabled")
}

func parseEcosystems(s string) []string {
	valid := make(map[string]bool, len(allEcosystems))
	for _, e := range allEcosystems {
		valid[e] = true
	}
	seen := make(map[string]bool)
	var result []string
	for _, e := range strings.Split(s, ",") {
		e = strings.TrimSpace(strings.ToLower(e))
		if valid[e] && !seen[e] {
			result = append(result, e)
			seen[e] = true
		}
	}
	return result
}

// buildRules generates the pf anchor content for the given ecosystems.
// Pass rules appear before block rules — required by pf evaluation order.
func buildRules(ecosystems []string, proxyPort int, proxyUser string) string {
	var sb strings.Builder
	sb.WriteString("# Escrow redirect rules — generated by escrow-cli\n\n")

	// rdr rules: one per host per ecosystem.
	for _, eco := range ecosystems {
		fmt.Fprintf(&sb, "# %s\n", eco)
		for _, host := range registryHosts[eco] {
			fmt.Fprintf(&sb, "rdr pass inet proto tcp from any to %s port 443 -> 127.0.0.1 port %d\n",
				host, proxyPort)
		}
	}
	sb.WriteString("\n")

	// pass rules BEFORE block rules.
	hosts := orderedHosts(ecosystems)
	sb.WriteString("# pass proxy upstream connections\n")
	for _, host := range hosts {
		fmt.Fprintf(&sb, "pass out quick proto tcp from any to %s port {80, 443} user %s\n",
			host, proxyUser)
	}
	sb.WriteString("\n")

	// block rules: prevent IPv6 and port-80 bypass.
	sb.WriteString("# block bypass: port 80 and IPv6\n")
	for _, host := range hosts {
		fmt.Fprintf(&sb, "block return out inet  proto tcp from any to %s port 80\n", host)
		fmt.Fprintf(&sb, "block return out inet6 proto tcp from any to %s port 80\n", host)
		fmt.Fprintf(&sb, "block return out inet6 proto tcp from any to %s port 443\n", host)
	}

	return sb.String()
}

// orderedHosts returns deduplicated hosts in ecosystem-insertion order.
func orderedHosts(ecosystems []string) []string {
	seen := make(map[string]bool)
	var hosts []string
	for _, eco := range ecosystems {
		for _, h := range registryHosts[eco] {
			if !seen[h] {
				seen[h] = true
				hosts = append(hosts, h)
			}
		}
	}
	return hosts
}
